// Shared layout sizes.

// Big cover art (the full player and the vinyl detail page) renders at this fixed max so it reads
// the same size across screens instead of scaling with the device. It only shrinks below this to
// fit narrow devices (width minus the page padding).
export const BIG_COVER_MAX = 324;

// Corner radius for a big cover (full player + vinyl detail), so they round identically.
export const BIG_COVER_RADIUS = 16;
